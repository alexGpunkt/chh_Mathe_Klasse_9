-- Dieser Index entspricht exakt dem Tracker-Aufruf:
--
-- on_conflict=student_id,unit,path
-- ============================================================

drop index if exists
  public.mathe9_progress_student_unit_path_uidx;

create unique index
  mathe9_progress_student_unit_path_uidx

on public.mathe9_progress (
  student_id,
  unit,
  path
);


