grant insert on public.mathe9_events to anon;
grant insert, update on public.mathe9_progress to anon;
grant select, insert, update, delete on public.mathe9_students to authenticated;
grant select on public.mathe9_events, public.mathe9_progress to authenticated;
grant usage, select on all sequences in schema public to anon, authenticated;
