-- ============================================================
-- Schüler-Freigabeliste
-- ============================================================

create table if not exists public.mathe9_students (
  id uuid
    primary key
    default gen_random_uuid(),

  login_name text
    not null,

  display_name text
    not null,

  class_code text
    not null,

  active boolean
    not null
    default true,

  created_at timestamptz
    not null
    default now(),

  updated_at timestamptz
    not null
    default now(),

  constraint mathe9_students_login_format
    check (
      login_name ~
      '^[^[:space:].]+\.[^[:space:].]+$'
    ),

  constraint mathe9_students_login_group_unique
    unique (
      login_name,
      class_code
    )
);


-- ============================================================
-- Einzelereignisse
-- ============================================================

create table if not exists public.mathe9_events (
  id bigint
    generated by default as identity
    primary key,

  event_type text
    not null,

  student_id uuid
    references public.mathe9_students(id)
    on delete set null,

  student text
    not null
    default 'anonym',

  device_id uuid
    not null,

  session_id uuid
    not null,

  class_code text,

  page text,

  unit text,

  path text,

  task text,

  payload jsonb
    not null
    default '{}'::jsonb,

  ts timestamptz
    not null
    default now()
);


-- ============================================================
-- Zusammengefasster Lernfortschritt
-- ============================================================

create table if not exists public.mathe9_progress (
  id bigint
    generated by default as identity
    primary key,

  student_id uuid
    references public.mathe9_students(id)
    on delete cascade,

  student text
    not null
    default 'anonym',

  device_id uuid
    not null,

  session_id uuid
    not null,

  class_code text,

  page text,

  unit text
    not null,

  path text
    not null
    default '',

  current_task text,

  completed_tasks integer
    not null
    default 0,

  total_tasks integer
    not null
    default 0,

  progress_percent integer
    not null
    default 0
    check (
      progress_percent
      between 0 and 100
    ),

  correct_count integer
    not null
    default 0,

  attempts_count integer
    not null
    default 0,

  status text
    not null
    default 'active',

  ts timestamptz
    not null
    default now(),

  updated_at timestamptz
    not null
    default now()
);


