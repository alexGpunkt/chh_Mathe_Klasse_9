-- Mathe9
-- Freigabeliste, Tracking und langfristiger Lernfortschritt
--
-- Dieses Skript ist für eine erneute Ausführung vorbereitet.
-- Vorhandene Tabellen und Daten bleiben grundsätzlich erhalten.
--
-- Bei mehrfach vorhandenen Fortschrittszeilen für dieselbe
-- Kombination aus Schüler, Einheit und Lernpfad bleibt der
-- zuletzt aktualisierte Datensatz erhalten.
-- ============================================================

