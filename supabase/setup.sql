-- ============================================================
-- Login-Prüfung ohne Offenlegung der Schülerliste
-- ============================================================

create or replace function
  public.mathe9_validate_student_login(
    p_login_name text,
    p_class_code text
  )

returns table (
  student_id uuid,
  login_name text,
  display_name text,
  class_code text
)

language sql

security definer

set search_path = public

stable

as $$

  select
    student.id,
    student.login_name,
    student.display_name,
    student.class_code

  from public.mathe9_students as student

  where
    lower(student.login_name) =
      lower(trim(p_login_name))

    and student.class_code =
      trim(p_class_code)

    and student.active = true

  limit 1;

$$;


-- ============================================================
-- Rechte der Login-Funktion
-- ============================================================

revoke all
on function
  public.mathe9_validate_student_login(
    text,
    text
  )
from public;


grant execute
on function
  public.mathe9_validate_student_login(
    text,
    text
  )
to anon, authenticated;


