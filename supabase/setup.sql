alter table public.mathe9_progress
  drop constraint if exists
  mathe9_progress_device_id_session_id_unit_path_key;

alter table public.mathe9_progress
  drop constraint if exists
  mathe9_progress_device_unit_path_key;


-- ============================================================
-- Bestehende Werte normalisieren
--
-- Diese Anweisungen sind vor allem für ältere Tabellenstände
-- gedacht, in denen einzelne Felder noch NULL sein könnten.
-- ============================================================

update public.mathe9_progress
set
  path = coalesce(path, ''),
  completed_tasks = greatest(
    0,
    coalesce(completed_tasks, 0)
  ),
  total_tasks = greatest(
    0,
    coalesce(total_tasks, 0)
  ),
  progress_percent = least(
    100,
    greatest(
      0,
      coalesce(progress_percent, 0)
    )
  ),
  correct_count = greatest(
    0,
    coalesce(correct_count, 0)
  ),
  attempts_count = greatest(
    0,
    coalesce(attempts_count, 0)
  ),
  status = coalesce(
    nullif(trim(status), ''),
    'active'
  ),
  updated_at = coalesce(
    updated_at,
    ts,
    now()
  );


-- ============================================================
-- Vorhandene Fortschritts-Dubletten bereinigen
--
-- Eindeutig sein soll:
--
--   student_id + unit + path
--
-- Wenn dieselbe Kombination mehrfach vorkommt, bleibt:
--
--   1. der zuletzt aktualisierte Datensatz,
--   2. bei identischem Zeitpunkt der Datensatz mit der
--      höchsten ID.
--
-- Datensätze ohne student_id werden hier nicht verändert.
-- ============================================================

with ranked_progress as (
  select
    id,

    row_number() over (
      partition by
        student_id,
        unit,
        path

      order by
        updated_at desc nulls last,
        ts desc nulls last,
        id desc
    ) as duplicate_position

  from public.mathe9_progress

  where student_id is not null
)

delete from public.mathe9_progress as progress

using ranked_progress as ranked

where
  progress.id = ranked.id
  and ranked.duplicate_position > 1;


-- ============================================================
-- Eindeutiger Schlüssel für den Fortschritts-Upsert
